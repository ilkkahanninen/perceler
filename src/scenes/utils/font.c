void font_draw(const Font *font, unsigned char *buf, int x, int y,
               unsigned char color, const char *s)
{
  int cx = x;
  int last = font->first_char + font->num_chars;
  int bytes_per_row = font->glyph_w >> 3;
  int glyph_stride = bytes_per_row * font->glyph_h;

  while (*s)
  {
    int c = (unsigned char)*s++;
    int idx = c - font->first_char;
    if (idx >= 0 && c < last)
    {
      const unsigned char *g = font->glyphs + idx * glyph_stride;
      int row;
      for (row = 0; row < font->glyph_h; row++)
      {
        int py = y + row;
        const unsigned char *row_bytes;
        unsigned char *dst_row;
        int b;
        if (py < 0 || py >= VGA_HEIGHT)
          continue;
        row_bytes = g + row * bytes_per_row;
        dst_row = buf + (unsigned)py * VGA_WIDTH;
        for (b = 0; b < bytes_per_row; b++)
        {
          unsigned char bits = row_bytes[b];
          int base_col;
          int bit;
          if (bits == 0)
            continue;
          base_col = cx + (b << 3);
          for (bit = 0; bit < 8; bit++)
          {
            int px = base_col + bit;
            if (px >= 0 && px < VGA_WIDTH && (bits & (1 << bit)))
              dst_row[px] = color;
          }
        }
      }
    }
    cx += font->glyph_w;
  }
}

int font_width(const Font *font, const char *s)
{
  return (int)strlen(s) * font->glyph_w;
}

/*
 * .fnt binary layout (little-endian int32s, matching tools/font.py):
 *     offset  size  field
 *       0     4    glyph_w
 *       4     4    glyph_h
 *       8     4    first_char
 *      12     4    num_chars
 *      16     N    glyph data (num_chars * (glyph_w/8) * glyph_h bytes)
 */
Font *font_load(Asset asset)
{
  unsigned char *raw;
  const int *hdr;
  Font *font;
  unsigned char *glyphs;
  int glyph_w, glyph_h, first_char, num_chars;
  unsigned long glyph_bytes;

  raw = (unsigned char *)data_read(asset);
  if (!raw)
    return NULL;

  if (asset.length < 16)
  {
    free(raw);
    return NULL;
  }

  hdr = (const int *)raw;
  glyph_w = hdr[0];
  glyph_h = hdr[1];
  first_char = hdr[2];
  num_chars = hdr[3];

  if (glyph_w <= 0 || (glyph_w & 7) != 0 || glyph_h <= 0 ||
      first_char < 0 || num_chars <= 0)
  {
    free(raw);
    return NULL;
  }

  glyph_bytes = (unsigned long)num_chars * (unsigned long)(glyph_w / 8) *
                (unsigned long)glyph_h;
  if ((unsigned long)asset.length < 16UL + glyph_bytes)
  {
    free(raw);
    return NULL;
  }

  font = (Font *)malloc(sizeof(Font));
  glyphs = (unsigned char *)malloc(glyph_bytes);
  if (!font || !glyphs)
  {
    free(raw);
    free(font);
    free(glyphs);
    return NULL;
  }

  memcpy(glyphs, raw + 16, glyph_bytes);
  free(raw);

  font->glyphs = glyphs;
  font->glyph_w = glyph_w;
  font->glyph_h = glyph_h;
  font->first_char = first_char;
  font->num_chars = num_chars;
  return font;
}

void font_free(Font *font)
{
  if (!font)
    return;
  free((void *)font->glyphs);
  free(font);
}
